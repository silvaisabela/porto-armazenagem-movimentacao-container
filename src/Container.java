
public class Container {

	private int id;
	private int contMove;
	
	public Container(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public int getContMove() {
		return contMove;
	}
	
	public void setContMove(int contMove) {
		this.contMove = contMove;
	}

}
